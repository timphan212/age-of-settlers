    public void setupProductionArea(String attacker, String defender) {
        this.attacker = attacker;
        this.defender = defender;
        List<TerrainTiles> defenderTerrains = new ArrayList<>();
        
        if(defender.compareTo("Norse") == 0) {
            Norse norsePlayer = Norse.getInstance();
            defenderTerrains = norsePlayer.getNorseTerrains();
        }
        else if(defender.compareTo("Greek") == 0){
            Greek greekPlayer = Greek.getInstance();
            defenderTerrains = greekPlayer.getGreekTerrains();
        }
        else {
            Egyptian egyptianPlayer = Egyptian.getInstance();
            defenderTerrains = egyptianPlayer.getEgyptianTerrains();
        }
        
        setTerrainTiles(defenderTerrains);
    }
    
    private void setTerrainTiles(List<TerrainTiles> terrainList) {
        Component[] terrainComponents = TerrainPanel.getComponents();
        int count = 0;
        
        for(Component terrainComponent : terrainComponents) {
            if(terrainComponent instanceof javax.swing.JPanel) {
                javax.swing.JPanel panel = (javax.swing.JPanel) terrainComponent;
                javax.swing.JLabel label = (javax.swing.JLabel) panel.getComponent(0);
                if(count < terrainList.size()) {
                    TerrainTiles terrain = terrainList.get(count);
                    label.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resources/images/" + terrain.getFileName())));
                    label.getAccessibleContext().setAccessibleDescription(Integer.toString(count));
                    count++;
                }
                else {
                    label.setVisible(false);
                }
            }
        }
    }
    
